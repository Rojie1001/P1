package rojie.poo.ifsc.P1;

public class TarefasRealizadas {
	private String assuntoR;
	private String materiaR;

	public TarefasRealizadas(String assuntoR, String materiaR) {
		super();
		this.assuntoR = assuntoR;
		this.materiaR = materiaR;
	}

	public String getAssuntoR() {
		return assuntoR;
	}

	public void setAssuntoR(String assuntoR) {
		this.assuntoR = assuntoR;
	}

	public String getMateriaR() {
		return materiaR;
	}

	public void setMateriaR(String materiaR) {
		this.materiaR = materiaR;
	}

}
