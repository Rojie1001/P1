package rojie.poo.ifsc.P1;

public class TarefasEspera {
	private String assuntoE;
	private String materiaE;

	public TarefasEspera(String assuntoE, String materiaE) {
		super();
		this.assuntoE = assuntoE;
		this.materiaE = materiaE;
	}

	public String getAssuntoE() {
		return assuntoE;
	}

	public void setAssuntoE(String assuntoE) {
		this.assuntoE = assuntoE;
	}

	public String getMateriaE() {
		return materiaE;
	}

	public void setMateriaE(String materiaE) {
		this.materiaE = materiaE;
	}

}
